from google.colab import drive
drive.mount('/content/drive')

import json
import csv

with open('/content/drive/MyDrive/Datasets/The Elder Scrolls/dataset_original.json', 'r') as file:
    data = json.load(file)

text_values = []

for url, content_info in data.items():
    text = content_info.get("text")
    if text:
        text_values.append(text)

csv_file_path = '/content/drive/MyDrive/Datasets/The Elder Scrolls/cleaned_data.csv'

with open(csv_file_path, 'w', newline='') as csvfile:
    csv_writer = csv.writer(csvfile)

    for text in text_values:
        csv_writer.writerow([text])